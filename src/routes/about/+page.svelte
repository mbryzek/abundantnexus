	<h4>Origin Story: The Abundant Nexus</h4>
	<p>
		<strong>Dr. Janusz Bryzek</strong> (1947 – 2022) was at the nexus of MEMS, entrepreneurism, and abundance.
		Janusz was always driven. As a young engineer in the Soviet Bloc, Janusz set his sights on escaping
		to the West in the 1970's. Through careful planning and sleight of hand, the Bryzek family of four
		made it safely to the US, but had to start afresh with few connections and only $300 in their pocket.
		Leveraging his engineering connections, Janusz quickly found work. But his entrepreneurial ambitions
		motivated him to look for new opportunities. Together with Kurt Petersen and Joe Mallon, Janusz
		co-founded Novasensor in 1985. The trio leveraged the marketing prowess of Roger Grace to grow the
		fledgling company. And grow it did! Novasensor is one of the first successful MEMS-based companies
		and a major contender in the pressure sensor market. They have now delivered over 800 million sensors
		worldwide. Ever the entrepreneur and MEMS maverick, Janusz went on to cofound another 10 companies
		commercializing MEMS devices ranging from pressure sensors to accelerometers, from gyros to resonators,
		and from micromirrors to pMUTs (Transparent Networks, LV Sensors, Jyve, Exo Imaging, etc.).
	</p>
	<p>
		Despite all his credentials as a technologist and entrepreneur, most remember Janusz' optimism
		and desire to apply technology for the benefit of all humanity. He was a firm believer in the
		concept that a rising tide lifts all boats, and he applied himself to making sure his
		technologies amplified this tide. This philosophy became epitomized by the concept of abundance.
		Abundance can be defined as having a positive and optimistic attitude towards life and the
		future, where individuals believe that there are ample opportunities and resources available to
		them, and that with effort and innovation, they can create a better future for themselves and
		others. Janusz fully embraced that concept in both his personal life and his business career, and
		paid it forward to family, friends, colleagues and the countless companies that he advised and
		encouraged.
	</p>
	<p>
		Exo Imaging was Janusz' final startup, and perhaps his ultimate expression of abundance. 75% of
		the world does not have ready access to medical imaging. Janusz and fellow cofounders Sandeep
		Akkaraju, Yusuf Haque, and John Kokulis imagined applying the exponential scale of MEMS pMUT
		(piezoelectric micromachined ultrasonic transducer) technology to enable low cost, portable
		ultrasound probes for the price of a cell phone. Exo is poised to transform medical imaging,
		bringing Janusz' vision to life.
	</p>
